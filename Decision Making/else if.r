#In case you need multiple checks, you can use multiple else if statements.
#For example, let's output the English version of the given number:


num <- 3
if(num == 1) {
  print("One")
} else if(num == 2) {
  print("Two")
} else if (num == 3) {
  print("Three")
} else {
  print("Something else")
}