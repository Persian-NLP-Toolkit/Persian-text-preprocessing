#The length function can be used to find the number of elements in a vector:

name <- c("reza", "ali", "navid")

print(length(name))

#The sum function returns the sum of all elements:

rev <- c(1, 4, 5, 6, 7, 8)
print(sum(rev))

#The sort function can be used to sort elements of a vector.

print(sort(rev))

