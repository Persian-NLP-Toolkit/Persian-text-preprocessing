#R allows us to create a sequence of numbers by using a colon and specifying the lower and upper bounds. The sequence in the code above will include the numbers 1 to 10.
#During each iteration of the for loop, the x variable will take the value of the next number in the sequence, thus, the resulting output will be the numbers 1 to 10.

for(x in 1:10){
  print(x)
}

